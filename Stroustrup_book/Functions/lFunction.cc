//lFunction- C++ Library Function

#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double number, squareRoot;
    number = 25.0;
    // sqrt() is a library function to calculate the square root
    squareRoot = sqrt(number);
    cout << "Sqaure root of " << number << " is: " << squareRoot << endl;

    return 0;
}
